void tl_on_recv_config(void *tl, tl_on_recv_t on_recv, void *on_recv_arg)
{
	tl_t *tran_layer;

	tran_layer = (tl_t *)tl;

	tran_layer->on_recv = on_recv;
	tran_layer->on_recv_arg = on_recv_arg;
}

uint16_t tl_send(void *tl, const uint8_t *data, uint16_t size)
{
	tl_t *tran_layer;

	tran_layer = (tl_t *)tl;

	return tran_layer->send(tran_layer, data, size);
}

